/**
 * Obtenir la langue actuelle
 */
function getCurrentLanguage() {
    return localStorage.getItem('language') || 
           localStorage.getItem('kivira_language') || 
           localStorage.getItem('app_language') ||
           document.documentElement.lang || 
           'fr';
}

/**
 * Traduire une clé
 */
function t(key) {
    const lang = getCurrentLanguage();
    return translation[lang]?.[key] || translation['fr']?.[key] || key;
}

/**
 * Traduire tous les éléments avec data-i18n dans un conteneur
 */
function translateElement(element) {
    const lang = getCurrentLanguage();
    
    // Traduire l'élément lui-même s'il a data-i18n
    const key = element.getAttribute('data-i18n');
    if (key && translation[lang]?.[key]) {
        element.textContent = translation[lang][key];
    }
    
    // Traduire tous les enfants avec data-i18n
    const children = element.querySelectorAll('[data-i18n]');
    children.forEach(child => {
        const childKey = child.getAttribute('data-i18n');
        if (childKey && translation[lang]?.[childKey]) {
            child.textContent = translation[lang][childKey];
        }
    });
}

/**
 * Traduire toute la page/modal
 */
function translatePage() {
    const lang = getCurrentLanguage();
    
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translation[lang]?.[key]) {
            element.textContent = translation[lang][key];
        }
    });
}

/**
 * Écouter les changements de langue
 */
function initTranslationListener() {
    // Écouter les changements de langue
    window.addEventListener('languageChanged', function() {
        translatePage();
    });
    
    // Écouter les changements dans localStorage
    window.addEventListener('storage', function(e) {
        if (e.key && (e.key.includes('language') || e.key.includes('lang'))) {
            translatePage();
        }
    });
}

// Initialiser
if (typeof window !== 'undefined') {
    initTranslationListener();
}

